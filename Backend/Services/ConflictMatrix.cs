    private static HashSet<(string, string)> BuildConflicts()
    {
        var set = new HashSet<(string, string)>();

        foreach (var (a, b) in ConflictPairs)
        {
            set.Add((a, b));
            set.Add((b, a));
        }

        return set;
    }

    public bool Conflicts(string lightA, string lightB)
    {
        return _conflicts.Contains((lightA, lightB));
    }

    public bool ConflictsWithAny(string lightId, IEnumerable<string> greenLights)
    {
        return greenLights.Any(green => Conflicts(lightId, green));
    }
